package matematica;


public class Numeros {
    
    public boolean numerosPar(int numero){
        return numero%2==0;
    }
    
    public int areaQuadrado(int lado){
        return lado*lado;
    }
    
    public Boolean numeroDivisivel(int dividendo,int divisor){
        return dividendo%divisor==0;
    }
}
